package itoro;

public class RelationalOperators {

	public static void main(String[] args) {
		
		int a =3,b=6,c=3, d=9;
		
		
		//Equal to
		
		System.out.println(a==b);
		
		System.out.println(a==c);
		
		//Not Equal
		
		System.out.println(a!=b);
		System.out.println(a!=c);
		
		
		//Greater than 
		
		System.out.println(a>b);
		System.out.println(b>c);

	}

}
